import 'package:equatable/equatable.dart';
import 'package:restaurant_app/domain/entity/category.dart';
import 'package:restaurant_app/domain/entity/customer_review.dart';
import 'package:restaurant_app/domain/entity/menu.dart';

class RestaurantDetail extends Equatable {
  const RestaurantDetail({
    required this.id,
    required this.name,
    required this.description,
    required this.city,
    required this.address,
    required this.pictureId,
    required this.categories,
    required this.menus,
    required this.rating,
    required this.customerReviews,
  });

  final String id;
  final String name;
  final String description;
  final String city;
  final String address;
  final String pictureId;
  final List<Category> categories;
  final Menus menus;
  final double rating;
  final List<CustomerReview> customerReviews;

  @override
  List<Object?> get props => [
        id,
        name,
        description,
        city,
        address,
        pictureId,
        categories,
        menus,
        rating,
        customerReviews,
      ];
}
